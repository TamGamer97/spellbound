const fs = require("fs");
const path = require("path");
const script = fs.readFileSync(path.join(__dirname, "cleanup-puzzles-2-pangrams.js"), "utf8");
const start = script.indexOf("BAD_PANGRAMS = new Set([");
const end = script.indexOf("].map((w) => w.toLowerCase());");
const block = script.slice(start, end);
const re = /"([a-z]+)"/g;
const words = [];
let m;
while ((m = re.exec(block))) words.push(m[1]);
const unique = [...new Set(words)].sort();
const outPath = path.join(__dirname, "..", "data", "bad-pangrams.txt");
fs.writeFileSync(outPath, unique.join("\n"), "utf8");
console.log("Wrote", unique.length, "words to data/bad-pangrams.txt");
